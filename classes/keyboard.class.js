/**
 * The Keyboard class serves as a simple way to handle keyboard input.
 * Each property in this class represents a specific key on the keyboard (LEFT, RIGHT, UP, DOWN, SPACE, D).
 * The boolean value of each property represents whether the corresponding key is currently pressed or not.
 * When a key is pressed, its corresponding property would be set to `true`.
 * When the key is released, its corresponding property would be set back to `false`.
 *
 */
class Keyboard {
    LEFT = false;
    RIGHT = false;
    UP = false;
    DOWN = false;
    SPACE = false;
    D = false;
}
